		internal static double InternalGetNumericValue(int ch)
		{
			int num = ch >> 8;
			if ((uint)num < (uint)NumericLevel1Index.Length)
			{
				num = NumericLevel1Index[num];
				num = NumericLevel2Index[(num << 4) + ((ch >> 4) & 0xF)];
				num = NumericLevel3Index[(num << 4) + (ch & 0xF)];
				ref byte source = ref Unsafe.AsRef(in NumericValues[num * 8]);
				if (BitConverter.IsLittleEndian)
				{
					return Unsafe.ReadUnaligned<double>(ref source);
				}
				return BitConverter.Int64BitsToDouble(BinaryPrimitives.ReverseEndianness(Unsafe.ReadUnaligned<long>(ref source)));
			}
			return -1.0;
		}

		internal static byte InternalGetDigitValues(int ch, int offset)
		{
			int num = ch >> 8;
			if ((uint)num < (uint)NumericLevel1Index.Length)
			{
				num = NumericLevel1Index[num];
				num = NumericLevel2Index[(num << 4) + ((ch >> 4) & 0xF)];
				num = NumericLevel3Index[(num << 4) + (ch & 0xF)];
				return DigitValues[num * 2 + offset];
			}
			return byte.MaxValue;
		}

