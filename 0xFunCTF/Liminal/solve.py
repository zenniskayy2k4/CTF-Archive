# Helpers
def permute_forward(val):
    # Output[i] = Input[Table[i]] logic in forward direction
    # This simulates the binary's P-Box
    res = 0
    for i in range(64):
        bit_pos = p_bytes[i] & 0x3F
        bit = (val >> bit_pos) & 1
        res |= (bit << i)
    return res

def sub_bytes_aes(val):
    res = 0
    for i in range(8):
        byte = (val >> (i * 8)) & 0xFF
        new_byte = sbox[byte]
        res |= (new_byte << (i * 8))
    return res

