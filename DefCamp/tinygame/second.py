# Dữ liệu được mã hóa trích xuất từ địa chỉ 0x00106840
encrypted_data = [
    0xf5, 0xdb, 0xa6, 0x60, 0x2f, 0x45, 0x74, 0x21, 0x12, 0x2c, 0xc4, 0x7d, 0x2b, 0x23, 0x90, 0x28,
    0x7a, 0x2e, 0xd6, 0xf0, 0xf4, 0x26, 0xdd, 0x4e, 0xda, 0x7a, 0x11, 0x3e, 0xc3, 0x3e, 0xc7, 0x39
]

# Khóa XOR dài 8 byte từ địa chỉ 0x001076c8
key = [0xa5, 0x1f, 0xc3, 0x77, 0x19, 0xee, 0x42, 0x58]

# Bảng chỉ mục (thứ tự đọc byte) từ địa chỉ 0x001079b8
indices = [
    0x1e, 0x11, 0x14, 0x17, 0x19, 0x01, 0x07, 0x1f, 0x1c, 0x0b, 0x02, 0x05,
    0x09, 0x12, 0x0d, 0x03, 0x0a, 0x15, 0x13, 0x08, 0x0f, 0x16, 0x06, 0x1d,
    0x0e, 0x04, 0x00, 0x1a, 0x0c, 0x18, 0x10, 0x1b
]

decrypted_bytes = []
# Vòng lặp 32 lần (tương đương độ dài của flag part B)
for i in range(32):
    # 1. Lấy chỉ mục đã bị xáo trộn
    shuffled_index = indices[i]
    
    # 2. Lấy byte dữ liệu được mã hóa từ chỉ mục đó
    encrypted_byte = encrypted_data[shuffled_index]
    
    # 3. Lấy byte khóa (lặp lại sau mỗi 8 byte)
    key_byte = key[i % 8]
    
    # 4. Thực hiện XOR để giải mã
    decrypted_byte = encrypted_byte ^ key_byte
    
    decrypted_bytes.append(decrypted_byte)

# Nối các byte đã giải mã thành chuỗi kết quả
second_part = "".join([chr(b) for b in decrypted_bytes])

print(f"[*] Found Second Part: {second_part}")