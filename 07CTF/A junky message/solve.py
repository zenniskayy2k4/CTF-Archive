# Bước 1: Xây dựng chuỗi nhị phân
# '~' -> '0'
# '=' -> '1'
# Các ký tự khác ('.', '-', '_') bị bỏ qua
binary_string = ""
for char in encrypted_message:
    if char == '~':
        binary_string += '0'
    elif char == '=':
        binary_string += '1'

# Bước 2: Chuyển đổi chuỗi nhị phân sang văn bản ASCII
decrypted_message = ""
# Lặp qua chuỗi nhị phân, mỗi lần lấy 8 bit (1 byte)
for i in range(0, len(binary_string), 8):
    # Lấy ra 1 byte
    byte = binary_string[i:i+8]
    # Chuyển byte đó từ dạng nhị phân sang số thập phân
    decimal_value = int(byte, 2)
    # Chuyển số thập phân sang ký tự ASCII và ghép vào kết quả
    decrypted_message += chr(decimal_value)

# In kết quả
