target_data = [
    0x4a, 0x41, 0x5a, 0x4f, 0x4a, 0x58, 0x69, 0x83, 0x83, 0x5e, 0x61, 0x23, 0x21, 0x7f, 0x67, 0x64,
    0x79, 0x67, 0x64, 0x66, 0x4f, 0x2f, 0x50, 0x45, 0x3b, 0x57, 0x3a, 0x4d, 0x21, 0x47, 0x1f, 0x2f,
    0x59, 0x3a, 0x40, 0x23, 0x28, 0x41, 0x42, 0x45, 0x42, 0xd8, 0xd4, 0xd4, 0x01, 0x1b, 0xce, 0x20,
    0xd7, 0xd9, 0xdb, 0xda, 0xc8, 0x0d
]

length = 0x36

flag = ""

# Lặp qua từng byte
for i in range(length):
    # Lấy byte mục tiêu tại vị trí i
    target_byte = target_data[i]
    
    # Áp dụng công thức đảo ngược
    # input[i] = (target[i] - i) ^ (i * 3 + 6)
    
    # 1. Đảo ngược phép cộng: (target_byte - i)
    #    Dùng & 0xFF để đảm bảo kết quả luôn là một byte (0-255), mô phỏng số học của kiểu char trong C
    temp = (target_byte - i) & 0xFF
    
    # 2. Đảo ngược phép XOR
    original_byte = temp ^ ((i * 3 + 6) & 0xFF)
    
    # Chuyển byte kết quả thành ký tự và nối vào flag
    flag += chr(original_byte)

print(f"Flag: {flag}")